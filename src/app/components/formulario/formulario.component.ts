  handleInputChange(e) {
    var file = e.dataTransfer ? e.dataTransfer.files[0] : e.target.files[0];
    var pattern = /image-*/;
    var reader = new FileReader();
    if (!file.type.match(pattern)) {
      alert('invalid format');
      return;
    }
    reader.onload = this._handleReaderLoaded.bind(this);
    reader.readAsDataURL(file);
  }
  _handleReaderLoaded(e) {
    let reader = e.target;
    this.imageSrc = reader.result;
  }
  
  getTrack(){
    this._conferenciaService.buscarConferencia(this.track).subscribe(
      response=>{
        
        
        if(response.charla){
          this.trackInteresado = response.charla
        }
        
      }
    )
  }

  getTracks(){
    this._userService.getTraks().subscribe(
      response=>{
        for( var i = 0; i < response.tracks.length; i++){ 
          if ( response.tracks[i].titulo === 'Sin Track') {
            response.tracks.splice(i, 1); 
          }
       }
        this.tracks = response.tracks    
        
      }
    )
  }

  onSubmit() {
    var idTrack

    if (this.track == null) {
      idTrack = this.user.interesado 
    }else{
      idTrack = this.track
      this.user.interesado = this.track
    }
    this.user.image = this.imageSrc
    this._userService.registerFollow(idTrack, this.user).subscribe(
      response => {
        if (response) {
          alert('Registrado Satisfactoriamente')
          this.status = 'OK'
          this.clean()
        }

      },
      error => {
        var errorMessage = <any>error;
        console.log(errorMessage)
        if (errorMessage != null) {
          this.status = 'error'
        }
      }
    )
  }

